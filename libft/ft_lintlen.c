#include "libft.h"

size_t		ft_lintlen(long nbr)
{
	size_t	len;

	len = 1;
	nbr < 0 ? len++ : len;
	while (nbr && nbr != nbr % 10)
	{
		len++;
		nbr = nbr / 10;
	}
	return (len);
}
